import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { menuItems, preferences, pastOrders, timeOfDay } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are a menu recommendation AI for a luxury hotel restaurant. Analyze the guest's preferences and past orders to suggest personalized dishes.

Return your response as a JSON object with this structure:
{
  "recommendations": [
    {
      "itemId": "string",
      "itemName": "string", 
      "reason": "string (brief explanation why this is recommended)",
      "confidence": "high" | "medium" | "low"
    }
  ],
  "personalNote": "string (a friendly personalized message to the guest)"
}

Consider:
- Dietary restrictions and preferences
- Past order patterns and favorites
- Time of day (breakfast/lunch/dinner appropriate items)
- Seasonal and chef recommendations
- Variety from past orders

Limit to 3-4 recommendations.`;

    const userPrompt = `Guest preferences: ${JSON.stringify(preferences || {})}
Past orders: ${JSON.stringify(pastOrders || [])}
Current time of day: ${timeOfDay || 'evening'}
Available menu items: ${JSON.stringify(menuItems)}

Please suggest the best dishes for this guest.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: userPrompt },
        ],
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content || "";
    
    // Parse JSON from response
    let recommendations;
    try {
      // Extract JSON from potential markdown code blocks
      const jsonMatch = content.match(/```json\n?([\s\S]*?)\n?```/) || content.match(/({[\s\S]*})/);
      const jsonStr = jsonMatch ? jsonMatch[1] : content;
      recommendations = JSON.parse(jsonStr);
    } catch {
      recommendations = {
        recommendations: [],
        personalNote: "We'd love to help you find something delicious today!"
      };
    }

    return new Response(JSON.stringify(recommendations), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (error) {
    console.error("Menu suggestions error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
